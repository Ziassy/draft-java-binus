    private String[] getListBooks() {
        List<String> kode_buku = new ArrayList<>();
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT id_books FROM buku");
            
            while(rs.next()) {
                String bookId = rs.getString("id_books");
                kode_buku.add(bookId);
            }
                     
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat get data: " + e.getMessage());
        }
        return kode_buku.toArray(String[]::new);
    }
    
    private void getDataBooks() {
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM buku WHERE id_books = '" + cmbIdBook.getSelectedItem().toString() + "'");
            
            LocalDate currentDate = LocalDate.now();
            String today = currentDate.toString();

            while(rs.next()) {
                String nama_buku = rs.getString("nama_buku");
                String harga = rs.getString("harga");

                inputBookName.setText(nama_buku);
                inputHarga.setText(harga);
                inputBookName.setEditable(false);
                inputHarga.setEditable(false);
                inputTglPeminjaman.setText(today);
                inputTglPengembalian.setText(today);
                
            }
                     
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat get data: " + e.getMessage());
        }
    }

   
