    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        String kode_buku = cmbIdBook.getSelectedItem().toString();
        String nama_buku = inputBookName.getText();
        String tanggal_pinjam = inputTglPeminjaman.getText();
        String tanggal_kembali = inputTglPengembalian.getText();
        double harga = Double.parseDouble(inputHarga.getText());
        
        rowCount = model.getRowCount();
        
        if(rowCount == 0) {
           model.addRow(new Object[]{kode_buku, nama_buku, tanggal_pinjam, tanggal_kembali, harga});
        } else {
            model.addRow(new Object[]{kode_buku, nama_buku, tanggal_pinjam, tanggal_kembali, harga});
            int lastRowIndex = model.getRowCount() - 1;
            model.moveRow(lastRowIndex, lastRowIndex, 0);
        }
                
        grandTotal += harga;
        txtGrandTotal.setText(String.valueOf(grandTotal));
        
        
    }//GEN-LAST:event_btnAddItemActionPerformed

