    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        String query = "INSERT INTO sewa (id_books, id_customer, id_karyawan, tgl_pinjam, tgl_pengembalian, harga) VALUES (?, ?, ?, ?, ?, ?)";

        try {
            rowCount = model.getRowCount();
            if ( inputCustomer.getText().isEmpty() || inputKaryawan.getText().isEmpty() || rowCount == 0 ||  inputPay.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "ID Customer, ID Karyawan, Item order, dan jumlah bayar tidak boleh kosong!");
            } else {
                double bayar = Double.parseDouble(inputPay.getText());
                grandTotal = Double.parseDouble(txtGrandTotal.getText());
                double kembalian = bayar - grandTotal;
                if (kembalian < 0) {
                    JOptionPane.showMessageDialog(null, "Kurang bayar!!!!! Rp. " + kembalian);
                    showRefund.setText("");
                } else {
                    showRefund.setText(Double.toString(kembalian));
                    
                    String cek_id_sewa = "SELECT * FROM sewa WHERE id_sewa = '" + inputSewa.getText() + "'";
                    st = cn.createStatement();
                    rs = st.executeQuery(cek_id_sewa);

                    if(rs.next()) {
                        JOptionPane.showMessageDialog(null, "ID Sewa Sudah terdaftar");
                    } else {
                        for (int countRowOrder = 0; countRowOrder < rowCount; countRowOrder++) {
                              
                              Object value_kode_buku = model.getValueAt(countRowOrder, 0);
                              Object value_tanggal_pinjam = model.getValueAt(countRowOrder, 2);
                              Object value_tanggal_kembali = model.getValueAt(countRowOrder, 3);
                              Object value_harga = model.getValueAt(countRowOrder, 4);
                              
                              if (value_kode_buku != null && value_tanggal_pinjam != null && value_tanggal_kembali != null && value_harga != null) {
                                   String kode_buku = value_kode_buku.toString();
                                   String id_customer = inputCustomer.getText();
                                   String id_karyawan = inputKaryawan.getText();
                                   String tanggal_pinjam = value_tanggal_pinjam.toString();
                                   String tanggal_kembali = value_tanggal_kembali.toString();
                                   double harga = Double.parseDouble(value_harga.toString());

                                   PreparedStatement preparedStatement = cn.prepareStatement(query);

                                   preparedStatement.setString(1, kode_buku);
                                   preparedStatement.setString(2, id_customer);
                                   preparedStatement.setString(3, id_karyawan);
                                   preparedStatement.setString(4, tanggal_pinjam);
                                   preparedStatement.setString(5, tanggal_kembali);
                                   preparedStatement.setDouble(6, harga);

                                   preparedStatement.executeUpdate();
                               }                             
                        }

                        JOptionPane.showMessageDialog(null, "Data Berhasil di simpan");
                    }
                }   
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

